class Gallery extends Component {
  state = {imageId: imagesList[0].imageUrl, altText: imagesList[0].imageAltText}

  onClickChange = valueId => {
    this.setState({
      imageId: imagesList[valueId].imageUrl,
      altText: imagesList[valueId].imageAltText,
    })
  }

  render() {
    const {imageId, altText} = this.state

    return (
      <div className="bg-container">
        <div className="galley-container">
          <img src={imageId} className="selected-image" alt={altText} />
          <h1 className="main-heading">Nature Photography</h1>
          <p className="description">Nature Photography by Rahul</p>
        </div>
        <ul className="thumbnail-list">
          {imagesList.map(eachImage => (
            <ThumbnailItem
              singleImage={eachImage}
              key={eachImage.id}
              onClickChange={this.onClickChange}
              isActive={imageId === eachImage.imageUrl}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
